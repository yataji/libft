#include "libft.h"

int		ft_cpt(char const *s, char c)
{
	int		i;
	int		l;
	int		k;

	l = 0;
	i = 0;
	k = 0;
	while (s[i])
	{
		while (s[i] != c && s[i])
		{
			if (k == 0)
			{
				k = 1;
				l++;
			}
			i++;
		}
		while (s[i] == c && s[i])
		{
			k = 0;
			i++;
		}
	}
	return (l);
}
